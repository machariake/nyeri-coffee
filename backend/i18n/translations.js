/**
 * Get translation for a key
 * @param {string} section - Translation section (common, navigation, etc.)
 * @param {string} key - Translation key
 * @param {string} lang - Language code (en, sw, fr)
 * @returns {string} - Translated text
 */
const t = (section, key, lang = 'en') => {
  const sectionData = translations[section];
  if (!sectionData) return key;
  
  const langData = sectionData[lang] || sectionData.en;
  return langData[key] || key;
};

/**
 * Get all translations for a language
 * @param {string} lang - Language code
 * @returns {object} - All translations flattened
 */
const getAllTranslations = (lang = 'en') => {
  const result = {};
  
  Object.keys(translations).forEach(section => {
    const sectionData = translations[section][lang] || translations[section].en;
    result[section] = sectionData;
  });
  
  return result;
};

